#include "MathLibrary.h"

namespace MathLibrary
{
    float Arithmetic::Add(float a, float b)
    {
        return a + b;
    }

    float Arithmetic::Subtract(float a, float b)
    {
        return a - b;
    }

    float Arithmetic::Multiply(float a, float b)
    {
        return a * b;
    }

    float Arithmetic::Divide(float a, float b)
    {
        return a / b;
    }
}
